// L5 experiment probe #3 — full call through emnapi v2 against a real
// (SharedArrayBuffer-backed) wasm memory, simulating the split-worker
// case where host has the napi context + memory access.
//
// The setup mirrors what the production split-worker topology would do:
//   - Host worker: creates emnapi context, has a Memory view onto the
//     shared SAB.
//   - Wasm worker: would own the actual wasm.Instance with that
//     Memory.  Here we use a stub instance.
//   - "wasm" calls napi imports → host's emnapi functions execute,
//     reading args from / writing results to shared memory directly.
//
// Validates: emnapi v2's napi functions WORK when given shared memory
// access, without the napi calls happening in the same thread as the
// wasm instance.

import { createContext } from "@emnapi/runtime";
import { createNapiModule } from "@emnapi/core";

// ── 1. Minimal valid wasm Module — needed because emnapi.init checks
// `module instanceof WebAssembly.Module`.  Magic bytes + version only.
const wasmModule = new WebAssembly.Module(new Uint8Array([
  0x00, 0x61, 0x73, 0x6d, // \0asm
  0x01, 0x00, 0x00, 0x00, // version 1
]));

// ── 2. Shared memory.  In production: wasm worker creates this and
// posts it (the SAB) to host.  Both sides view it.
const memory = new WebAssembly.Memory({
  initial: 1,
  maximum: 1,
  shared: true,
});
// Can't pass SAB to Memory in this version; use the Memory's own SAB.
const memoryView = new Uint32Array(memory.buffer);

// ── 2. Stub wasm exports.  Has to be an actual `instance` with
// .exports.{memory,malloc,free,__indirect_function_table,...}.
//
// In production: this is the real wasm worker's instance, accessed via
// RPC stubs.  malloc/free are RPC stubs that call the wasm worker.
const stubTable = new WebAssembly.Table({ initial: 1, element: "anyfunc" });
const mallocCalls = [];
const freeCalls = [];
const stubInstance = {
  exports: {
    memory,
    malloc(size) {
      // Trivial bump allocator for the experiment.  Returns 256-byte
      // aligned addresses starting at 1024 (leave room for stack).
      mallocCalls.push(size);
      // Reserve space at offset 1024 onwards.
      const ptr = 1024 + (mallocCalls.length - 1) * 256;
      return ptr;
    },
    free(ptr) {
      freeCalls.push(ptr);
    },
    __indirect_function_table: stubTable,
    emnapi_create_env() {
      // Wasm-side counter for env IDs.  In production, the real wasm
      // exports this.  For experiment, return id 1 on first call.
      return 1;
    },
    emnapi_delete_env() {
      return 0;
    },
    // Required by emnapi v2 init.
    emnapi_runtime_init() {},
    emnapi_runtime_finalize() {},
    // The wasm-side module registration entry.  In a real native
    // addon, this is autogenerated by NAPI_MODULE_INIT / NAPI_MODULE
    // macros and returns the addon's exports object.  For experiment
    // we return undefined (handle id 0) — emnapi will treat it as
    // an empty module.
    napi_register_wasm_v1(_env) {
      return 0; // napi_value handle id; 0 means undefined
    },
  },
};

// ── 3. Host emnapi setup.  This is where the napi context + functions
// live.  In production this runs on the host worker.
const hostCtx = createContext();
const hostModule = createNapiModule({
  context: hostCtx,
  childThread: false,
  asyncWorkPoolSize: 0,
});

// Hook up the wasm instance into the host's napi module.
let initOK = false;
let initError = null;
try {
  hostModule.init({
    instance: stubInstance,
    module: wasmModule,
    memory,
    table: stubTable,
  });
  initOK = true;
} catch (e) {
  initError = e;
}

console.log("[host] napiModule.init:", initOK ? "OK" : `FAIL: ${initError?.message ?? initError}`);

if (!initOK) {
  console.log("\nFalling back: inspect what init expects...");
  console.log("  init function arity:", hostModule.init?.length);
}

// ── 4. Try to invoke a napi function via the host's table, with
// arguments that point into the SHARED memory.  This is what a wasm
// call would look like over RPC.

if (initOK) {
  const napi = hostModule.imports.napi;
  // First, create an env id.  napiModule.PThread would handle this
  // normally; we faked it via stubInstance.exports.emnapi_create_env.
  // The env id (1) was returned but emnapi's stores need to know about
  // it.  This is where things get complicated — emnapi expects to call
  // the wasm export emnapi_create_env which internally calls JS-side
  // code that registers the env.
  console.log("\n--- attempting napi_get_undefined via shared memory ---");
  try {
    const resultPtr = 2048; // arbitrary location in shared memory
    const status = napi.napi_get_undefined(1, resultPtr);
    console.log("  napi_get_undefined(env=1, resultPtr=2048) status:", status);
    if (status === 0) {
      const handle = memoryView[resultPtr / 4];
      console.log("  wrote handle id at memory[2048]:", handle);
    }
  } catch (e) {
    console.log("  threw:", e.message);
  }
}

console.log("\n--- summary ---");
console.log("  malloc calls:", mallocCalls.length);
console.log("  free calls:", freeCalls.length);
console.log("  ctx.refStore size:", hostCtx.refStore?.size ?? "n/a");

console.log("\n--- probe-with-memory.mjs: complete ---");
